/**
 * Get the effectiveness of a move against a pokemon with supplied types
 * @param {string} chartName Name of chart in typeCharts object/dict
 * @param {number} attackingType Type of the attacking move, 0-indexed in pokeapi.co order
 * @param {number} defenderTypes[] Type(s) of the defending pokemon, 0-indexed in pokeapi.co order
 * @returns {number} Damage multiplier
 */
export default function getTypeEffectiveness(chartName, attackingType, defenderTypes) {
    return defenderTypes.reduce((acc, defenderType) => acc * typeCharts[chartName][attackingType][defenderType], 1.0);
}
